/**
 * Type Coercion
 * string + number = string
 * number + string = string
 * 
 * In text inputs, the value of the inputs are always strings
 * If we want to enter numbers and use numbers we have to wrap
 * the input values in Number() 
 * For example if we entered 11 then we wrap the value in Number()
 * like so: Number(11) in our code.
 */

console.log('hello' + 5)
// output: 'hello5'
